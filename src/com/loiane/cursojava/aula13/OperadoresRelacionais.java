package com.loiane.cursojava.aula13;

public class OperadoresRelacionais {
    public static void main(String[] args) {

        /*
        *  Operadores Relacionais
        *  == igual a
        *  != diferente de
        *   > maior que
        *   < menor que
        *  >= maior ou igual que
        *  <= menor ou igual que
        *
         */




        int valor1 = 1;
        int valor2 = 2;


        System.out.println("Valor 1 == Valor 2:   "+ (valor1 == valor2));
        System.out.println("Valor 1 != Valor 2:   "+ (valor1 != valor2));
        System.out.println("Valor 1 > Valor 2:   "+ (valor1 > valor2));
        System.out.println("Valor 1 >= Valor 2:   "+ (valor1 >= valor2));
        System.out.println("Valor 1 < Valor 2:   "+ (valor1 < valor2));
        System.out.println("Valor 1 <= Valor 2:   "+ (valor1 <= valor2));

        // mais usados em Loops







    }

}
